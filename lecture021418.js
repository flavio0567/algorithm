var arr = [1];
function fact(num){
    if(num < 0){return null;}    
    for(var i=1; i<=num; i++){
        arr[i] = arr[i-1] * i;
    }
    return arr[num];
}
console.log('factorial:0', fact(0));     // 1 
console.log('factorial:15', fact(15));   // 1307674368000
console.log('factorial:170', fact(170)); // 7.257415615307994e+306
console.log('factorial:171', fact(171)); // Infinity
