function arrConcat(arr1, arr2) {
    var newArr = arr1;
    var k = arr1.length;
    for (var i = 0; i < arr2.length; i++) {
        newArr[i+k] = arr2[i];
    }
    return newArr;
}
var myArr = arrConcat([1,2,3,4], [55, 'a', 'ff']);
console.log(myArr); // [1, 2, 3, 4, 55, "a", "ff"]
